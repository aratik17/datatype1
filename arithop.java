package JAVA;

public class arithop {
    public static void main(String[] args){
        int a=10;
        int b=30;
        System.out.println("a+b:" +(a+b));
        System.out.println("a-b:" +(a-b));
        System.out.println("a*b:" +(a*b));
        System.out.println("a/b:" +(a/b));
        System.out.println("a%b:" +(a%b));
    }
}
